function initializeTrialForm() {
  const form = $("#trialForm");
  const status = $("#formStatus");
  const dateInput = $("#preferredDate");
  dateInput.min = new Date().toISOString().split("T")[0];

  function showError(field, message) {
    field.classList.add("invalid");
    const error = field.closest("label")?.querySelector(".field-error");
    if (error) error.textContent = message;
  }

  function clearError(field) {
    field.classList.remove("invalid");
    const error = field.closest("label")?.querySelector(".field-error");
    if (error) error.textContent = "";
  }

  $$("input, select, textarea", form).forEach((field) => {
    field.addEventListener("input", () => clearError(field));
    field.addEventListener("change", () => clearError(field));
  });

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    status.textContent = "";
    status.className = "form-status";

    const requiredFields = $$('[required]', form);
    let valid = true;

    requiredFields.forEach((field) => {
      clearError(field);
      if (!String(field.value).trim()) {
        showError(field, "This field is required.");
        valid = false;
      }
    });

    const email = form.elements.email;
    if (email.value && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email.value)) {
      showError(email, "Enter a valid email address.");
      valid = false;
    }

    const phone = form.elements.phone;
    if (phone.value && phone.value.replace(/\D/g, "").length < 8) {
      showError(phone, "Enter a valid mobile number.");
      valid = false;
    }

    if (!valid) {
      status.textContent = "Please check the highlighted fields.";
      status.classList.add("error");
      form.querySelector(".invalid")?.focus();
      return;
    }

    const data = new FormData(form);
    const message = [
      "Hi, I would like to book a free trial.",
      "",
      `Name: ${data.get("name")}`,
      `Phone: ${data.get("phone")}`,
      `Email: ${data.get("email") || "Not provided"}`,
      `Goal: ${data.get("goal")}`,
      `Program: ${data.get("program")}`,
      `Preferred Date: ${data.get("date")}`,
      `Preferred Time: ${data.get("time")}`,
      `Message: ${data.get("message") || "None"}`
    ].join("\n");

    form.classList.add("is-loading");
    form.querySelector('button[type="submit"]').disabled = true;

    setTimeout(() => {
      form.classList.remove("is-loading");
      form.querySelector('button[type="submit"]').disabled = false;
      status.textContent = "Your details are ready. Opening WhatsApp…";
      status.classList.add("success");
      safeWhatsAppOpen(message);
    }, 650);
  });
}

