function createWhatsAppLink(phoneNumber, message) {
  const cleanNumber = String(phoneNumber || "").replace(/\D/g, "");
  if (cleanNumber.length < 10) return "#";
  return `https://wa.me/${cleanNumber}?text=${encodeURIComponent(message)}`;
}

